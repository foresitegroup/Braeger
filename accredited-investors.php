Braeger Auto Finance Group provides financial support to independent used car dealers.<br>
<br>

In partnership with our accredited investors, we provide flexible financing to help maximize a dealer's credit line.  Through our loan program, we provide credit lines from $1.5 million and above.  We offer a variety of loan programs specifically geared to the borrowing needs of the independent used car dealer to help increase the efficiency of their consumer lending function, allowing the dealer to maximize their profits by maintaining their retail installment contracts.<br>
<br>

The goal of our note program is to offer our accredited investors the opportunity to take part in the returns of our loan programs with our participating dealers.  Since our capital is not tied to Wall Street demand and quotas, we are able to provide that capital to the independent dealer in a fashion that is attractive to the dealer while providing attractive returns for our accredited investors.<br>
<br>

Because of stressful lending conditions, even the highest quality independent dealer may become undercapitalized, which negatively affects their ability to acquire new capital.  As the economy and fiscal situation for independent dealers continues to improve, we have found that it is a fantastic time to help out the independent dealers and thereby benefit our accredited investors.<br>
<br>

Our note financing opportunity is truly a "win-win" situation for the dealers and our accredited investor partners.


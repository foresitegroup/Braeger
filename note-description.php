Auto Dealer Renewable Notes are subordinate corporate debt offered at varied interest rates and maturities by Braeger Auto Finance Group LLC.  The notes are unsecured assets of Braeger Auto Finance Group LLC, a secondary lender to independent automobile dealers.  Braeger Auto Finance Group LLC structures lending contracts with the finance company of independent auto dealers to help them maximize their credit lines.  Our company is an attractive alternative to traditional bank financing for independent auto dealers, most of whom are challenged in increasing their credit lines because of stringent lending standards caused by the recent financial crisis.<br>
<br>

Braeger Auto Finance Group LLC structures their lending contracts with the dealers and allows accredited investors and institutions to take part in the returns of the finance contracts.  Braeger Auto Finance Group LLC utilizes unbiased third-party valuation firms to confirm the value of an auto dealer prospect to calculate the value of their current loan portfolio to help ensure the dealer contract we have signed can be expected to cover the Auto Dealer Renewable Notes.<br>
<br>

Features and highlights of Auto Dealer Renewable Notes include:
<ul>
  <li>Fixed interest rates for the entire maturity term</li>
  <li>Four year maturity term</li>
  <li>Interest payments made on a quarterly basis</li>
</ul>

